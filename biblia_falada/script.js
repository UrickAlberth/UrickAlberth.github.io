let albumList = [];
let currentAlbumIndex = 0;
let currentAlbum = [];
let currentSongIndex = 0;

document.addEventListener('DOMContentLoaded', function() {
    displayAlbums(albums);
});

function displayAlbums(albums) {
    albumList = Object.keys(albums);
    currentAlbumIndex = 0;

    const albumsContainer = document.getElementById('albums-container');
    albumsContainer.innerHTML = '';

    albumList.forEach(album => {
        const albumElement = document.createElement('div');
        albumElement.textContent = album;
        albumElement.addEventListener('click', () => {
            currentAlbumIndex = albumList.indexOf(album);
            displaySongs(albums[album]);
        });
        albumsContainer.appendChild(albumElement);
    });

    displaySongs(albums[albumList[currentAlbumIndex]]);
}

function displaySongs(songs) {
    currentAlbum = songs;
    currentSongIndex = 0;

    const songsContainer = document.getElementById('songs-container');
    songsContainer.innerHTML = '';

    songs.forEach((song, index) => {
        const songElement = document.createElement('div');
        songElement.id = "id" + index;
        songElement.textContent = song;
        songElement.addEventListener('click', () => {
            currentSongIndex = index;
            document.getElementById('playPause').textContent = "| |";
            playSong(song, index);
        });
        songsContainer.appendChild(songElement);
    });
}

let AudioInPlay = false;
function playSong(song, index) {
    const audioPlayer = document.getElementById('audioPlayer');
    const albumName = albumList[currentAlbumIndex];
    audioPlayer.src = `BÍBLIA/${albumName}/${song}`;
    audioPlayer.play();
    if (AudioInPlay) {			
        AudioInPlay.classList.remove("Inplay");
    }
    AudioInPlay = document.getElementById("id" + index);
    AudioInPlay.classList.add("Inplay");
}

const btnPlayPause = document.getElementById('playPause');
const audioPlayer = document.getElementById('audioPlayer');

btnPlayPause.addEventListener('click', () => {
    if (audioPlayer.paused) {
        audioPlayer.play();
        btnPlayPause.textContent = "| |";
    } else {
        audioPlayer.pause();
        btnPlayPause.textContent = "▶";
    }
});

document.getElementById('prev').addEventListener('click', prevSong);
document.getElementById('next').addEventListener('click', nextSong);

function prevSong() {
    if (currentSongIndex > 0) {
        currentSongIndex--;
        document.getElementById('playPause').textContent = "| |";
        playSong(currentAlbum[currentSongIndex], currentSongIndex);
    }
}

function nextSong() {
    if (currentSongIndex < currentAlbum.length - 1) {
        currentSongIndex++;
    } else if (currentAlbumIndex < albumList.length - 1) {
        currentAlbumIndex++;
        currentAlbum = albums[albumList[currentAlbumIndex]];
        displaySongs(currentAlbum);
        currentSongIndex = 0;
    } else {
        currentAlbumIndex = 0;
        currentAlbum = albums[albumList[currentAlbumIndex]];
        currentSongIndex = 0;
    }

    document.getElementById('playPause').textContent = "| |";
    playSong(currentAlbum[currentSongIndex], currentSongIndex);
}

audioPlayer.addEventListener('ended', () => {
    nextSong();
});
// Função para manipular o menu hamburguer
// Função para alternar a visibilidade do menu
function toggleMenu() {
  const menu = document.querySelector(".menu");
  menu.classList.toggle("active");
}

// Evento de clique no ícone do menu hamburguer
const burgerIcon = document.querySelector(".burger");
burgerIcon.addEventListener("click", toggleMenu);

// Evento de clique nos itens do menu (para fechar o menu após clicar em um link)
const menuItems = document.querySelectorAll(".menu li a");
menuItems.forEach((item) => {
  item.addEventListener("click", () => {
    // Verifica se o menu está aberto antes de fechá-lo
    const menu = document.querySelector(".menu");
    if (menu.classList.contains("active")) {
      toggleMenu();
    }
  });
});


// Função para carregar os projetos no HTML
function carregarProjetos() {
  const projetosList = document.getElementById("projetos-list");

  // Iterar sobre cada projeto no objeto "projetos"
  for (const key in projetos) {
    if (Object.hasOwnProperty.call(projetos, key)) {
      const projeto = projetos[key];

      // Criar os elementos HTML para o projeto
      const li = document.createElement("li");
      const h3 = document.createElement("h3");
      const p = document.createElement("p");
      const a = document.createElement("a");
      const img = document.createElement("img");
      

      // Preencher os elementos com os dados do projeto
      h3.textContent = projeto.nome;
      p.textContent = projeto.descricao;
      a.textContent = "Ver Projeto";
      a.href = projeto.projetoUrl;
      a.target = "_blank";
      a.classList.add("project-link");
      img.src = projeto.imgUrl;
      img.width = 200;
      a.appendChild(img);
      
      

      // Adicionar os elementos ao <li> e <ul>
      li.appendChild(h3);
      li.appendChild(p);
      li.appendChild(a);

      projetosList.appendChild(li);
    }
  }
}

// Chame a função para carregar os projetos ao carregar a página
window.onload = carregarProjetos;

function showFullProject() {
  // Mostrar o conteúdo completo quando o botão for clicado
  const fullProjectDiv = document.querySelector(".full-project");
  fullProjectDiv.style.display = "block";

  // Esconder o botão após mostrar o conteúdo completo
  const continueButton = document.querySelector("button");
  continueButton.style.display = "none";
}
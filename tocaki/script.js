function validarData(data) {
  const regex = /^\d{4}-\d{2}-\d{2}$/; // expressão regular para validar formato yyyy-mm-dd
  if (!regex.test(data)) {// se a entrada não corresponder ao formato
    return false; // retornar false
  }
  const partes = data.split("-"); // separar a entrada em ano, mês e dia
  const ano = parseInt(partes[0]);
  const mes = parseInt(partes[1]);
  const dia = parseInt(partes[2]);
  if (ano < 1900 || ano > 2100) {// validar o ano
    return false;
  }
  if (mes < 1 || mes > 12) {// validar o mês
    return false;
  }
  if (dia < 1 || dia > 31) {// validar o dia
    return false;
  }
  return true; // se todas as validações passaram, retornar true
}
function formatarData(data) {
  if (!validarData(data)) {// validar a entrada do usuário
    return data;
  }
  const partes = data.split("-");
  const dia = partes[2];
  const mes = partes[1];
  const ano = partes[0];
  return `${dia}/${mes}/${ano}`;
}


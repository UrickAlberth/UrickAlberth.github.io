function enviomsg() {

  // Seleciona o botão de envio
  const sendButton = document.querySelector('#btmsg');

  // Seleciona o campo de entrada de mensagem
  const messageInput = document.querySelector('textarea');

  // Seleciona o contêiner de mensagens
  const messageContainer = document.querySelector('.message-container');
  // Adiciona um ouvinte de evento de clique ao botão de envio
  sendButton.addEventListener('click', () => {
    // Obtém o conteúdo do campo de entrada de mensagem
    const messageContent = messageInput.value;

    // Verifica se o campo de entrada de mensagem não está vazio
    if (messageContent.trim() !== '') {
      // Cria um novo elemento de mensagem
      const messageElement = document.createElement('div');
      messageElement.classList.add('message');

      // Cria elementos de parágrafo para o remetente e conteúdo da mensagem
      const senderElement = document.createElement('p');
      senderElement.classList.add('sender');
      senderElement.textContent = 'Você:';
      const contentElement = document.createElement('p');
      contentElement.textContent = messageContent;

      // Adiciona os elementos de parágrafo ao elemento de mensagem
      messageElement.appendChild(senderElement);
      messageElement.appendChild(contentElement);

      // Adiciona o elemento de mensagem ao contêiner de mensagens
      messageContainer.appendChild(messageElement);

      // Limpa o campo de entrada de mensagem
      messageInput.value = '';
    }
  });
}
